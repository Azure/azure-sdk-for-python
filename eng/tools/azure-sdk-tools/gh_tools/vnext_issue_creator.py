# This script is used to create issues for client libraries failing the vnext of mypy, pyright, and pylint.
from __future__ import annotations
from typing import Optional

import sys
import os
import subprocess
import logging
import datetime
import re
import calendar
import typing
import pathlib
from typing_extensions import Literal
from github import Github, Auth, GithubException

from ci_tools.variables import discover_repo_root

logging.getLogger().setLevel(logging.INFO)

CHECK_TYPE = Literal["mypy", "pylint", "pyright", "sphinx"]

# ---------------------------------------------------------------------------
# Auto-fix automation constants
# ---------------------------------------------------------------------------

#: Label constants for auto-fix state management.
LABEL_AUTO_FIX = "copilot-auto-fix"
LABEL_AUTO_FIX_DISABLED = "copilot-auto-fix-disabled"

#: Managed block markers for Copilot instructions in issue bodies.
COPILOT_AUTOFIX_START = "<!-- copilot-autofix:start -->"
COPILOT_AUTOFIX_END = "<!-- copilot-autofix:end -->"

#: Copilot coding-agent bot login.
DEFAULT_COPILOT_LOGIN = "copilot-swe-agent"


# ---------------------------------------------------------------------------
# Auto-fix helpers
# ---------------------------------------------------------------------------


def _resolve_copilot_node_id(issue, github_instance) -> Optional[str]:
    """Return the Copilot bot node ID from GitHub's assignable actor data."""
    login = DEFAULT_COPILOT_LOGIN
    issue_node_id = issue.raw_data["node_id"]

    # dynamically query GitHub for the assignable actor node ID for the Copilot bot
    try:
        _, data = github_instance._Github__requester.graphql_query(
            """
            query($assignableId: ID!, $login: String!) {
              node(id: $assignableId) {
                ... on Issue {
                  suggestedActors(first: 10, query: $login) {
                    nodes {
                      __typename
                      ... on Bot {
                        id
                        login
                      }
                    }
                  }
                }
              }
            }
            """,
            {"assignableId": issue_node_id, "login": login},
        )
        actors = data.get("data", {}).get("node", {}).get("suggestedActors", {}).get("nodes", [])
        for actor in actors:
            if actor.get("login", "").lower() == login.lower() and actor.get("id"):
                return actor["id"]
    except Exception as e:
        logging.warning(f"Failed to resolve Copilot node ID dynamically: {e}")

    logging.warning(f"Could not find {DEFAULT_COPILOT_LOGIN} in suggested actors for issue #{issue.number}")
    return None


def is_auto_fix_eligible(
    issue_labels: list[str],
) -> bool:
    """Return True when the package/check combination qualifies for auto-fix."""
    if LABEL_AUTO_FIX_DISABLED in issue_labels:
        return False
    return True


def find_existing_fix_prs(
    repo,
    issue_number: int,
    package_name: str,
    check_type: str,
) -> list:
    """Search for open PRs that likely address the same vnext failure.

    Returns a (possibly empty) list of matching PR objects.
    """
    matches = []
    try:
        open_prs = repo.get_pulls(state="open", sort="created", direction="desc")
        for pr in open_prs:
            body = pr.body or ""
            title = pr.title or ""
            search_text = f"{title} {body}".lower()

            # 1. PR explicitly references the issue number
            issue_ref = f"#{issue_number}"
            has_issue_ref = issue_ref in title or issue_ref in body

            # 2. PR mentions package + check type
            has_pkg_and_check = package_name.lower() in search_text and check_type.lower() in search_text

            if has_issue_ref or has_pkg_and_check:
                matches.append(pr)
    except GithubException as e:
        logging.warning(f"Failed to search PRs for duplicate detection: {e}")

    return matches


def build_copilot_instructions(package_path: str, check_type: str) -> str:
    """Build the Copilot auto-fix instruction block for the issue body."""
    skill_name = f"fix-{check_type}"

    inner = (
        f"## Copilot instructions:\n\n"
        f"Use the `{skill_name}` skill to resolve `{check_type}` failures "
        f"in `{package_path}`.\n"
        f"Do not make unrelated formatting, changelog, version, or "
        f"generated-code changes.\n\n"
        f"Open a PR that links this issue and includes:\n\n"
        f"> Automated Fix: This PR was automatically generated by Copilot "
        f"in response to a vnext compatibility issue.\n\n"
        f"If a safe fix is not possible, describe the "
        f"attempted commands, the failure category, and the recommended "
        f"manual next step."
    )
    return f"\n\n{COPILOT_AUTOFIX_START}\n{inner}\n{COPILOT_AUTOFIX_END}"


def _upsert_copilot_instructions(body: str, instructions: str) -> str:
    """Replace existing Copilot instruction block or append if absent.

    Uses managed HTML-comment markers to locate the block, preserving any
    human-authored content outside the markers.
    """
    start_idx = body.find(COPILOT_AUTOFIX_START)
    end_idx = body.find(COPILOT_AUTOFIX_END)
    if start_idx != -1 and end_idx != -1:
        return body[:start_idx].rstrip() + instructions
    return body + instructions


def reconcile_auto_fix_labels(issue, eligible: bool) -> None:
    """Add or verify automation labels on the issue.

    Preserves all existing labels; only adds auto-fix labels when eligible.
    """
    current_labels = [lbl.name if hasattr(lbl, "name") else str(lbl) for lbl in issue.labels]

    if eligible:
        labels_to_add = []
        if LABEL_AUTO_FIX not in current_labels:
            labels_to_add.append(LABEL_AUTO_FIX)
        for label in labels_to_add:
            try:
                issue.add_to_labels(label)
                logging.info(f"Added label '{label}' to issue #{issue.number}")
            except GithubException as e:
                logging.warning(f"Failed to add label '{label}' to issue #{issue.number}: {e}")


def _is_copilot_already_assigned(issue) -> bool:
    """Check whether the Copilot login is already among the issue assignees."""
    for assignee in issue.assignees:
        name = assignee.login if hasattr(assignee, "login") else str(assignee)
        if name.lower() == DEFAULT_COPILOT_LOGIN.lower():
            return True
    return False


def _unassign_copilot(issue, github_instance, copilot_node_id: str) -> bool:
    """Remove the Copilot coding agent from the issue assignees.

    Uses the GraphQL ``removeAssigneesFromAssignable`` mutation.
    Treats "not currently assigned" as success (idempotent).
    """
    issue_node_id = issue.raw_data["node_id"]
    try:
        github_instance._Github__requester.graphql_named_mutation(
            "removeAssigneesFromAssignable",
            {
                "assignableId": issue_node_id,
                "assigneeIds": [copilot_node_id],
            },
            output_schema="assignable { ... on Issue { id } }",
        )
        logging.info(f"Unassigned {DEFAULT_COPILOT_LOGIN} from issue #{issue.number}")
        return True
    except Exception as e:
        logging.warning(f"Failed to unassign {DEFAULT_COPILOT_LOGIN} from issue #{issue.number}: {e}")
        return False


def assign_copilot(
    issue,
    github_instance,
    copilot_node_id: str,
    package_name: str,
    check_type: str,
    force_reassign: bool = False,
) -> bool:
    """Attempt to assign the Copilot coding agent to the issue.

    Uses the GraphQL ``addAssigneesToAssignable`` mutation because the
    Copilot bot (``copilot-swe-agent``) is not assignable via the REST
    assignees endpoint.

    When *force_reassign* is True and Copilot is already assigned, the
    agent is first unassigned then reassigned so that a new Copilot
    session is triggered (e.g. after a checker version bump).

    Returns True on success, False on failure after logging a warning.
    """
    if _is_copilot_already_assigned(issue):
        if not force_reassign:
            logging.info(f"Copilot already assigned to issue #{issue.number}, skipping")
            return True
        logging.info(f"Copilot already assigned to issue #{issue.number}, " f"re-assigning to trigger new session")
        if not _unassign_copilot(issue, github_instance, copilot_node_id):
            return False

    issue_node_id = issue.raw_data["node_id"]
    try:
        github_instance._Github__requester.graphql_named_mutation(
            "addAssigneesToAssignable",
            {
                "assignableId": issue_node_id,
                "assigneeIds": [copilot_node_id],
            },
            output_schema="assignable { ... on Issue { id } }",
        )
        logging.info(f"Assigned {DEFAULT_COPILOT_LOGIN} to issue #{issue.number} for {package_name}/{check_type}")
        return True
    except Exception as e:
        logging.warning(f"Failed to assign {DEFAULT_COPILOT_LOGIN} to issue #{issue.number}: {e}")
        return False


def _try_auto_fix(
    repo,
    issue,
    github_instance,
    package_name: str,
    package_path: str,
    check_type: str,
    issue_labels: list[str],
    version_changed: bool = False,
) -> None:
    """Run the auto-fix eligibility → duplicate check → assign flow.

    When *version_changed* is True (e.g. checker version bump), Copilot is
    unassigned and reassigned so a fresh session picks up the new errors.
    """
    eligible = is_auto_fix_eligible(issue_labels)

    if not eligible:
        return

    # Duplicate PR detection
    matching_prs = find_existing_fix_prs(repo, issue.number, package_name, check_type)
    if matching_prs:
        pr_urls = ", ".join(pr.html_url for pr in matching_prs)
        logging.info(f"Skipping Copilot assignment for issue #{issue.number}: " f"matching PR(s) found: {pr_urls}")
        return

    # Upsert Copilot instructions (replace existing block or append)
    body = issue.body or ""
    instructions = build_copilot_instructions(package_path, check_type)
    updated_body = _upsert_copilot_instructions(body, instructions)
    try:
        issue.edit(body=updated_body)
    except GithubException as exc:
        logging.warning(
            "Failed to update Copilot instructions for issue #%s: %s",
            issue.number,
            exc,
        )

    copilot_node_id = _resolve_copilot_node_id(issue, github_instance)
    if not copilot_node_id:
        return

    # Assign Copilot (force reassignment on version bumps)
    if assign_copilot(
        issue, github_instance, copilot_node_id, package_name, check_type, force_reassign=version_changed
    ):
        reconcile_auto_fix_labels(issue, eligible=True)


def get_version_running(check_type: CHECK_TYPE) -> str:
    commands = [sys.executable, "-m", check_type, "--version"]
    version = subprocess.run(
        commands,
        check=True,
        capture_output=True,
    )
    version = version.stdout.rstrip().decode("utf-8")
    version_running = re.findall(r"(\d+.\d+.\d+)", version)[0]
    logging.info(f"Running {check_type} version {version_running}")
    return version_running


def get_build_link(check_type: CHECK_TYPE) -> str:
    build_id = os.getenv("BUILD_BUILDID")
    job_id = os.getenv("SYSTEM_JOBID")

    next_id: str
    if check_type == "mypy":
        next_id = "c4b2a078-69a7-55a2-d776-67715c71590f"
    if check_type == "pyright":
        next_id = "d243185e-b901-5eef-29fe-f7943e030451"
    if check_type == "pylint":
        next_id = "b33d1587-3539-5735-af43-e3e62f02ca4b"
    if check_type == "sphinx":
        next_id = "82919efa-82d6-5dc4-2e9a-f82117bff292"

    return (
        f"https://dev.azure.com/azure-sdk/internal/_build/results?buildId={build_id}&view=logs&j={job_id}&t={next_id}"
    )


def get_merge_dates(year: int) -> typing.List[datetime.date]:
    """We'll merge the latest version of the type checker/linter quarterly
    on the Monday after release week. This function returns those 4 Mondays
    for the given year.
    """
    c = calendar.Calendar(firstweekday=calendar.FRIDAY)
    first = c.monthdatescalendar(year, 1)
    second = c.monthdatescalendar(year, 4)
    third = c.monthdatescalendar(year, 7)
    fourth = c.monthdatescalendar(year, 10)

    merge_months = [first, second, third, fourth]

    merge_dates = []
    for month in merge_months:
        code_complete = [
            day for week in month for day in week if day.weekday() == calendar.FRIDAY and day.month in [1, 4, 7, 10]
        ][0]
        monday_after_release_week = code_complete + datetime.timedelta(days=10)
        merge_dates.append(monday_after_release_week)
    return merge_dates


def get_date_for_version_bump(today: datetime.date) -> str:
    merge_dates = get_merge_dates(today.year)
    try:
        merge_date = min(date for date in merge_dates if date >= today)
    except ValueError:
        # today's date is after October merge date, so rollover to next year
        merge_dates = get_merge_dates(today.year + 1)
        merge_date = min(date for date in merge_dates if date >= today)
    return merge_date.strftime("%Y-%m-%d")


def get_labels(package_name: str, service: str) -> tuple[list[str], list[str]]:
    repo_root = discover_repo_root()
    codeowners_path = pathlib.Path(repo_root) / ".github" / "CODEOWNERS"
    with open(codeowners_path, "r") as codeowners_file:
        codeowners = codeowners_file.readlines()

    label = ""
    service_label = ""
    service_owners = []
    labels = []
    assignees = []
    if "mgmt" in package_name:
        labels.append("Mgmt")
    for line in codeowners:
        if line.startswith("# PRLabel:"):
            label = line.split("# PRLabel: %")[1].strip()
        if label and line.startswith("/sdk/"):
            parts = [part for part in line.split("@")[0].split("/") if part.strip()][1:]
            if len(parts) > 2:
                continue
            try:
                service_directory = parts[0]
            except IndexError:
                # it was a single file
                continue
            try:
                library = parts[1]
                if package_name == library:
                    labels.append(label)
                    # Extract codeowners from the line
                    tokens = line.split()
                    owners = [token.lstrip("@") for token in tokens if token.startswith("@")]
                    # Filter out teams (containing '/')
                    assignees = [owner for owner in owners if "/" not in owner]
                    return labels, assignees
            except IndexError:
                if service_directory == service:
                    service_label = label
                    # Extract service-level codeowners
                    tokens = line.split()
                    owners = [token.lstrip("@") for token in tokens if token.startswith("@")]
                    service_owners = [owner for owner in owners if "/" not in owner]

    if service_label:
        labels.append(service_label)
        assignees = service_owners
    return labels, assignees


def create_vnext_issue(package_dir: str, check_type: CHECK_TYPE, check_version: Optional[str] = None) -> None:
    """This is called when a client library fails a vnext check.
    An issue is created with the details or an existing issue is updated with the latest information."""

    package_dir_path = pathlib.Path(package_dir)
    package_name = package_dir_path.name
    service_directory = package_dir_path.parent.name
    auth = Auth.Token(os.environ["GH_TOKEN"])
    g = Github(auth=auth)

    today = datetime.date.today()
    repo = g.get_repo("Azure/azure-sdk-for-python")

    issues = repo.get_issues(state="open", labels=[check_type], creator="azure-sdk")  # type: ignore[arg-type]
    vnext_issue = [issue for issue in issues if issue.title.split("needs")[0].strip() == package_name]

    version = check_version or get_version_running(check_type)
    build_link = get_build_link(check_type)
    merge_date = get_date_for_version_bump(today)
    error_type = "linting" if check_type == "pylint" else "docstring" if check_type == "sphinx" else "typing"
    guide_link = (
        "[Pylint Guide](https://github.com/Azure/azure-sdk-for-python/blob/main/doc/dev/pylint_checking.md)"
        if check_type == "pylint"
        else (
            "[Sphinx and docstring checker](https://github.com/Azure/azure-sdk-for-python/blob/main/doc/eng_sys_checks.md#sphinx-and-docstring-checker)"
            if check_type == "sphinx"
            else "[Typing Guide](https://github.com/Azure/azure-sdk-for-python/blob/main/doc/dev/static_type_checking.md#run-mypy)"
        )
    )

    repo_root = discover_repo_root()
    title = f"{package_name} needs {error_type} updates for {check_type} version {version}"
    template = (
        f"**ACTION NEEDED:** This version of {check_type} will be merged on **{merge_date}**. "
        f"The build will begin to fail for this library if errors are not fixed."
        f"\n\n**Library name:** {package_name}"
        f"\n**{check_type.capitalize()} version:** {version}"
        f"\n**{check_type.capitalize()} Build:** [Link to build ({today.strftime('%Y-%m-%d')})]({build_link})"
        f"\n**How to fix:** Run the `next-{check_type}` azpysdk command at the library package-level and resolve "
        f"the {error_type} errors.\n"
        f"1) `{repo_root}>pip install -e ./eng/tools/azure-sdk-tools`\n"
        f"2) `../{package_name}>azpysdk next-{check_type} --isolate .`\n\n"
        f"See the {guide_link} for more information."
    )

    package_path = f"sdk/{service_directory}/{package_name}"

    # create an issue for the library failing the vnext check
    if not vnext_issue:
        try:
            labels, assignees = get_labels(package_name, service_directory)
        except Exception as e:
            logging.warning(f"Failed to get labels and assignees from CODEOWNERS for {package_name}: {e}")
            labels = []
            assignees = []
            if package_name.startswith("azure-mgmt-"):
                labels.append("Mgmt")

        labels.extend([check_type])
        logging.info(f"Issue does not exist for {package_name} with {check_type} version {version}. Creating...")
        issue = repo.create_issue(title=title, body=template, labels=labels)

        # Assign codeowners individually with error handling
        for assignee in assignees:
            try:
                issue.add_to_assignees(assignee)
                logging.info(f"Assigned {assignee} to issue for {package_name}")
            except GithubException as e:
                logging.warning(f"Failed to assign {assignee} to issue for {package_name}: {e}")

        # Auto-fix: check eligibility and assign Copilot
        issue_label_names = [lbl if isinstance(lbl, str) else lbl.name for lbl in issue.labels]
        _try_auto_fix(repo, issue, g, package_name, package_path, check_type, issue_label_names)
        return

    # an issue exists, let's update it so it reflects the latest typing/linting errors
    logging.info(f"Issue exists for {package_name} with {check_type} version {version}. Updating...")
    try:
        labels, assignees = get_labels(package_name, service_directory)
    except Exception as e:
        logging.warning(f"Failed to get labels and assignees from CODEOWNERS for {package_name}: {e}")
        labels = []
        assignees = []

    # Detect version change so Copilot can be re-triggered
    old_title = vnext_issue[0].title
    vnext_issue[0].edit(
        title=title,
        body=template,
    )
    version_changed = old_title != title

    # Assign codeowners individually with error handling
    for assignee in assignees:
        try:
            vnext_issue[0].add_to_assignees(assignee)
            logging.info(f"Assigned {assignee} to issue for {package_name}")
        except GithubException as e:
            logging.warning(f"Failed to assign {assignee} to issue for {package_name}: {e}")

    # Auto-fix: reconcile labels and retry assignment if no matching PR
    issue_label_names = [lbl.name if hasattr(lbl, "name") else str(lbl) for lbl in vnext_issue[0].labels]
    _try_auto_fix(
        repo,
        vnext_issue[0],
        g,
        package_name,
        package_path,
        check_type,
        issue_label_names,
        version_changed=version_changed,
    )


def close_vnext_issue(package_name: str, check_type: CHECK_TYPE) -> None:
    """This is called when a client library passes a vnext check. If an issue exists for the library, it is closed."""

    auth = Auth.Token(os.environ["GH_TOKEN"])
    g = Github(auth=auth)

    repo = g.get_repo("Azure/azure-sdk-for-python")

    issues = repo.get_issues(state="open", labels=[check_type], creator="azure-sdk")  # type: ignore[arg-type]
    vnext_issue = [issue for issue in issues if issue.title.split("needs")[0].strip() == package_name]
    if vnext_issue:
        logging.info(f"{package_name} passes {check_type}. Closing existing GH issue #{vnext_issue[0].number}...")
        vnext_issue[0].edit(state="closed")
