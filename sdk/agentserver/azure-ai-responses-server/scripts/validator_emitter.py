"""Emitter that builds deterministic Python validator modules from schemas."""

from __future__ import annotations

from typing import Any


def _sanitize_identifier(name: str) -> str:
    normalized = "".join(ch if ch.isalnum() else "_" for ch in name)
    while "__" in normalized:
        normalized = normalized.replace("__", "_")
    normalized = normalized.strip("_")
    return normalized or "schema"


def _resolve_ref(ref: str) -> str:
    return ref.rsplit("/", 1)[-1]


def _ordered(value: Any) -> Any:
    if isinstance(value, dict):
        return {k: _ordered(value[k]) for k in sorted(value)}
    if isinstance(value, list):
        return [_ordered(v) for v in value]
    return value


def _header() -> str:
    return (
        "# <auto-generated>\n"
        "# This file was generated by scripts/generate_validators.py from an OpenAPI spec.\n"
        "# Do not edit manually. Re-run validator generation to regenerate.\n"
        "# </auto-generated>\n"
    )


def build_validator_module(schemas: dict[str, dict[str, Any]], roots: list[str]) -> str:
    """Build generated validator module source code."""
    ordered_schemas = _ordered(schemas)
    ordered_roots = sorted(dict.fromkeys(roots))

    lines: list[str] = [_header(), "", "from __future__ import annotations", "", "from typing import Any", ""]

    lines.append(f"SCHEMAS = {repr(ordered_schemas)}")
    lines.append("")
    lines.append("ROOT_SCHEMAS = " + repr(ordered_roots))
    lines.append("")

    lines.extend(
        [
            "def _resolve_ref(ref: str) -> str:",
            "    return ref.rsplit('/', 1)[-1]",
            "",
            "def _type_name(schema: dict[str, Any]) -> str:",
            "    if '$ref' in schema:",
            "        return _resolve_ref(schema['$ref'])",
            "    t = schema.get('type')",
            "    return str(t) if isinstance(t, str) else 'value'",
            "",
            "def _matches_type(value: Any, expected: str) -> bool:",
            "    if expected == 'string':",
            "        return isinstance(value, str)",
            "    if expected == 'integer':",
            "        return isinstance(value, int) and not isinstance(value, bool)",
            "    if expected == 'number':",
            "        return (isinstance(value, int) and not isinstance(value, bool)) or isinstance(value, float)",
            "    if expected == 'boolean':",
            "        return isinstance(value, bool)",
            "    if expected == 'object':",
            "        return isinstance(value, dict)",
            "    if expected == 'array':",
            "        return isinstance(value, list)",
            "    return True",
            "",
            "def _validate_schema(value: Any, schema: dict[str, Any], path: str, errors: list[dict[str, str]]) -> None:",
            "    if schema.get('nullable') and value is None:",
            "        return",
            "",
            "    if '$ref' in schema:",
            "        ref_name = _resolve_ref(schema['$ref'])",
            "        ref_schema = SCHEMAS.get(ref_name)",
            "        if isinstance(ref_schema, dict):",
            "            _validate_schema(value, ref_schema, path, errors)",
            "        return",
            "",
            "    if 'enum' in schema:",
            "        allowed = schema.get('enum', [])",
            "        if value not in allowed:",
            "            errors.append({'path': path, 'message': f\"Value '{value}' is not valid. Allowed: {', '.join(str(v) for v in allowed)}\"})",
            "",
            "    union_key = 'oneOf' if 'oneOf' in schema else 'anyOf' if 'anyOf' in schema else None",
            "    if union_key:",
            "        branches = schema.get(union_key, [])",
            "        matched = False",
            "        for branch in branches:",
            "            branch_errors: list[dict[str, str]] = []",
            "            if isinstance(branch, dict):",
            "                _validate_schema(value, branch, path, branch_errors)",
            "                if not branch_errors:",
            "                    matched = True",
            "                    break",
            "        if not matched:",
            "            expected = ', '.join(_type_name(b) for b in branches if isinstance(b, dict)) or 'valid branch'",
            "            errors.append({'path': path, 'message': f'Expected one of: {expected}'})",
            "        return",
            "",
            "    expected_type = schema.get('type')",
            "    if isinstance(expected_type, str) and not _matches_type(value, expected_type):",
            "        errors.append({'path': path, 'message': f'Expected {expected_type}'})",
            "        return",
            "",
            "    if expected_type == 'array' and isinstance(value, list):",
            "        item_schema = schema.get('items')",
            "        if isinstance(item_schema, dict):",
            "            for idx, item in enumerate(value):",
            "                _validate_schema(item, item_schema, f'{path}[{idx}]', errors)",
            "",
            "    if expected_type == 'object' and isinstance(value, dict):",
            "        properties = schema.get('properties', {})",
            "        required = schema.get('required', [])",
            "        if isinstance(properties, dict):",
            "            for field in required:",
            "                if field not in value:",
            "                    errors.append({'path': f'{path}.{field}', 'message': f\"Required property '{field}' is missing\"})",
            "",
            "            for field, field_schema in properties.items():",
            "                if field in value and isinstance(field_schema, dict):",
            "                    _validate_schema(value[field], field_schema, f'{path}.{field}', errors)",
            "",
            "        addl = schema.get('additionalProperties')",
            "        if isinstance(addl, dict):",
            "            known = set(properties.keys()) if isinstance(properties, dict) else set()",
            "            for key, item in value.items():",
            "                if key not in known:",
            "                    _validate_schema(item, addl, f'{path}.{key}', errors)",
            "",
            "        disc = schema.get('discriminator')",
            "        if isinstance(disc, dict):",
            "            prop = disc.get('propertyName', 'type')",
            "            mapping = disc.get('mapping', {})",
            "            disc_val = value.get(prop)",
            "            if not isinstance(disc_val, str):",
            "                errors.append({'path': f'{path}.{prop}', 'message': f\"Required discriminator '{prop}' is missing or invalid\"})",
            "                return",
            "            ref = mapping.get(disc_val)",
            "            if isinstance(ref, str):",
            "                _validate_schema(value, {'$ref': ref}, path, errors)",
            "",
            "def _validate_by_name(schema_name: str, payload: Any) -> list[dict[str, str]]:",
            "    errors: list[dict[str, str]] = []",
            "    schema = SCHEMAS.get(schema_name)",
            "    if not isinstance(schema, dict):",
            "        return errors",
            "    _validate_schema(payload, schema, '$', errors)",
            "    return errors",
            "",
        ]
    )

    target_roots = ordered_roots if ordered_roots else sorted(ordered_schemas)
    for name in target_roots:
        fn = f"validate_{_sanitize_identifier(name)}"
        lines.append(f"def {fn}(payload: Any) -> list[dict[str, str]]:")
        lines.append(f"    return _validate_by_name({name!r}, payload)")
        lines.append("")

    if not target_roots:
        lines.append("def validate_payload(payload: Any) -> list[dict[str, str]]:")
        lines.append("    _ = payload")
        lines.append("    return []")
        lines.append("")

    return "\n".join(lines).rstrip() + "\n"
