import os
from azure.purview.scanning import PurviewScanningClient
from azure.identity import DefaultAzureCredential
from dotenv import load_dotenv

"""
# PREREQUISITES
    pip install azure-identity
    pip install azure-purview-scanning
# USAGE
    python usages_list.py

    Before run the sample, please set the values of the client ID, tenant ID and client secret
    of the AAD application as environment variables: AZURE_CLIENT_ID, AZURE_TENANT_ID,
    AZURE_CLIENT_SECRET. For more info about how to get the value, please see:
    https://docs.microsoft.com/azure/active-directory/develop/howto-create-service-principal-portal
"""
load_dotenv()
endpoint = os.getenv("PURVIEWSCANNING_ENDPOINT")
client = PurviewScanningClient(endpoint=endpoint, credential=DefaultAzureCredential())
result = client.filters.get(data_source_name="myDataSourceName", scan_name="myScanName")
print(result)